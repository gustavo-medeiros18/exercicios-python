# Crie variáveis para armazenar um número inteiro, um número decimal e uma string.
# Verifique o tipo de dado de cada variável usando `type()`.

intValue = 10
floatValue = 10.5
stringValue = "Hello World"

# Comparações booleanas usando type()
isFloat = type(floatValue) == float
isInt = type(intValue) == int
isString = type(stringValue) == str

print(isFloat)  # True
print(isInt)    # True
print(isString) # True
